export type Language = keyof typeof translations
export const defaultLanguage: Language = 'es'

export function t(key: string, lang: Language = defaultLanguage): any {
  const keys = key.split('.')
  let value: any = translations[lang]
  
  for (const k of keys) {
    value = value?.[k]
  }
  
  return value || key
}

// Create a context-aware translation function
let currentLanguage: Language = defaultLanguage

export function setCurrentLanguage(lang: Language) {
  currentLanguage = lang
}

export function getCurrentLanguage(): Language {
  return currentLanguage
}

// Context-aware translation function
export function tc(key: string): any {
  return t(key, currentLanguage)
} 